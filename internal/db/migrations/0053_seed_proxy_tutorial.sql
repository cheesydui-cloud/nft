  '# 如何配置代理

本教程说明如何在本平台配置落地出口、创建转发规则，并把可用代理链接导入客户端。

---

## 一、先弄清几个概念

- **中转节点**：管理员授权给你的入口 / 链路节点，在「我的规则」里选作入口。
- **落地节点**：流量最终要到达的代理出口，创建规则时选「出口节点」。
- **直连**：不经中转、直接使用的原始代理，出现在「我的代理」→「直连」。
- **中转**：经规则改写入口后的代理链接，在「我的规则」复制，或「我的代理」→「中转」。

> **落地**可作为规则出口；**直连**会出现在「我的代理」；**未配置**不参与任何功能。同一节点可以同时标记落地和直连。

---

## 二、准备落地 / 直连节点

打开左侧 **概览**。页面中有 **「我的代理 URI」** 卡片。

### 方式 A：手动填写 URI

1. 在 **手动填写** 文本框中粘贴代理链接，一行一条，例如：
   - `vless://…`
   - `trojan://…`
   - 或 Clash 风格：`🇭🇰 Name = snell, host, port, psk = xxx, version = 5`
2. 点击 **保存**（数据只保存在当前浏览器，不会上传到服务器）。
3. 展开 **配置表**，给每个节点点选用途：
   - 需要做规则出口 → 点亮 **落地**
   - 需要直接复制使用 → 点亮 **直连**

### 方式 B：订阅地址

1. 点击 **订阅地址** 展开输入框。
2. 粘贴订阅 URL（可多行），点击 **更新订阅**。
3. 在节点列表里同样标记 **落地** / **直连**（可勾选后批量设置）。

### 方式 C：管理员已分配落地源

若管理员已为你配置落地订阅或节点，可直接打开 **落地节点** 查看列表；仍可在概览页用本地 URI 覆盖同地址节点。

配置完成后：

- **落地节点** 页：查看可用出口（来源「本地」或「分配」）
- **我的代理** 页：查看已标记为 **直连** 的节点

---

## 三、创建转发规则

1. 打开 **我的规则**，点击右上角 **＋ 创建规则**。
2. 填写表单：
   - **节点**：选择管理员授权的入口节点（单点 / 组合）
   - 若入口后还有中间层要求，按提示选择 **线路层**
   - **协议**：TCP / UDP / TCP+UDP
   - **转发模式**（或组合时的 **出口段模式**）：
     - **内核态**：线路稳定、低丢包时性能更好
     - **用户态**：跨境或不稳定网络更抗抖动（仅 TCP；UDP 自动走内核态）
   - **入口端口**（可选）：留空则自动分配
   - **出口节点**：从已标记为 **落地** 的列表中选择
   - **备注**（可选）
3. 点击 **保存**。

> 若提示「尚无可用出口节点」，请先回概览页添加 URI 并标记 **落地**，或联系管理员分配落地源。

---

## 四、复制链接并导入客户端

1. 在 **我的规则** 列表中，对目标规则点 **复制**。
2. 顶栏可切换复制格式：
   - **URI**：原始代理链接
   - **YAML**：Clash 节点片段
3. 也可打开 **我的代理**：
   - **直连**：原始 URI
   - **中转**：按规则改写入口后的中转 URI
   - 支持 **复制全部**
4. 将复制内容粘贴到 Clash / Surge / 其他客户端并启用。

---

## 五、推荐完整流程（中转）

1. 概览 → 添加代理 URI 或更新订阅
2. 标记目标节点为 **落地**（需要直连再用时再点 **直连**）
3. 我的规则 → 创建规则（入口节点 + 落地出口）
4. 复制规则链接（URI 或 YAML）
5. 导入客户端使用

---

## 六、常见问题

**Q：保存了 URI，创建规则时出口列表仍为空？**  
A：节点必须标记为 **落地**。「未配置」不会出现在出口选择里。

**Q：换电脑 / 清浏览器后本地 URI 没了？**  
A：手动填写与本地角色只存在本浏览器。请重新粘贴，或改用订阅地址 / 请管理员分配落地源。

**Q：复制后客户端连不上？**  
A：检查规则入口节点是否在线、账号是否过期/超额、落地节点是否到期，以及客户端协议是否与链接一致。

**Q：只想用原始代理、不要中转？**  
A：把节点标为 **直连**，在 **我的代理 → 直连** 复制即可，无需创建规则。

**Q：如何同时中转和直连同一个节点？**  
A：配置表里可同时点亮 **落地** 与 **直连**。
