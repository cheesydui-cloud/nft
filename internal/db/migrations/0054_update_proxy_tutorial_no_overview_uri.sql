本教程说明如何在本平台使用落地出口、创建转发规则，并把可用代理链接导入客户端。

---

## 一、先弄清几个概念

- **中转节点**：管理员授权给你的入口 / 链路节点，在「我的规则」里选作入口。
- **落地节点**：流量最终要到达的代理出口，创建规则时选「出口节点」。
- **直连**：不经中转、直接使用的原始代理，出现在「我的代理」→「直连」。
- **中转**：经规则改写入口后的代理链接，在「我的规则」复制，或「我的代理」→「中转」。

> **落地**可作为规则出口；**直连**会出现在「我的代理」。同一节点可以同时具备落地与直连用途（由管理员配置来源与用途）。

---

## 二、查看可用落地节点

打开左侧 **落地节点**，查看管理员为你分配的出口列表。

1. 列表展示名称、协议、地址、到期时间与来源。
2. 若管理员配置了动态订阅，可点击 **刷新订阅** 拉取最新节点。
3. 需要原始链接时，可在行内 **复制**。

> 若列表为空，请联系管理员为你分配落地节点或订阅来源。

配置完成后：

- **落地节点** 页：查看可用出口
- **我的代理** 页：查看可用直连 / 中转代理（有数据时才会出现在侧栏）

---

## 三、创建转发规则

1. 打开 **我的规则**，点击右上角 **＋ 创建规则**。
2. 填写表单：
   - **节点**：选择管理员授权的入口节点（单点 / 组合）
   - 若入口后还有中间层要求，按提示选择 **线路层**
   - **协议**：TCP / UDP / TCP+UDP
   - **转发模式**（或组合时的 **出口段模式**）：
     - **内核态**：线路稳定、低丢包时性能更好
     - **用户态**：跨境或不稳定网络更抗抖动（仅 TCP；UDP 自动走内核态）
   - **入口端口**（可选）：留空则自动分配
   - **出口节点**：从已分配的 **落地** 列表中选择
   - **备注**（可选）
3. 点击 **保存**。

> 若提示「尚无可用出口节点」，请联系管理员分配落地源。

---

## 四、复制链接并导入客户端

1. 在 **我的规则** 列表中，对目标规则点 **复制**。
2. 顶栏可切换复制格式：
   - **URI**：原始代理链接
   - **YAML**：Clash 节点片段
3. 也可打开 **我的代理**（侧栏有入口时）：
   - **直连**：原始 URI
   - **中转**：按规则改写入口后的中转 URI
   - 支持 **复制全部**
4. 将复制内容粘贴到 Clash / Surge / 其他客户端并启用。

---

## 五、推荐完整流程（中转）

1. 落地节点 → 确认管理员已分配可用出口
2. 我的规则 → 创建规则（入口节点 + 落地出口）
3. 复制规则链接（URI 或 YAML）
4. 导入客户端使用

---

## 六、常见问题

**Q：落地节点列表是空的？**  
A：需要管理员为你分配落地节点或订阅来源。

**Q：创建规则时出口列表为空？**  
A：同样需要管理员先分配落地源；仅有入口授权不够。

**Q：复制后客户端连不上？**  
A：检查规则入口节点是否在线、账号是否过期/超额、落地节点是否到期，以及客户端协议是否与链接一致。

**Q：只想用原始代理、不要中转？**  
A：在 **我的代理 → 直连** 复制即可（有直连数据时侧栏会出现该入口），无需创建规则。

**Q：概览页上的「我的配额 / 公告 / 已授权节点」是干什么的？**  
A：用于查看账号额度、公告与已授权中转节点，不再用于配置代理 URI。
